const menu = document.getElementById("opts");
document.addEventListener("change", function () {
  const selectedValue = menu.value;
  console.log(selectedValue);
  if (selectedValue === "class12") {
    deleteAllCards();
    create(class12);
  } else if (selectedValue === "sport") {
    deleteAllCards();
    create(Sports);
  } else if (selectedValue === "ug") {
    deleteAllCards();
    create(GraduationStudies);
  } else if (selectedValue === "disab") {
    deleteAllCards();
    create(Disab);
  }
});
function deleteAllCards() {
  const parentElement = document.querySelector(".content-main");
  while (parentElement.firstChild) {
    parentElement.removeChild(parentElement.firstChild);
  }
}
function create(school) {
  const parentElement = document.querySelector(".content-main");

  const numberOfDivs = school.length;

  for (let i = 0; i < numberOfDivs; i++) {
    const cardDiv = document.createElement("div");
    cardDiv.className = "info-card";
    const upperDiv = document.createElement("div");
    upperDiv.className = "upper";
    cardDiv.appendChild(upperDiv);
    const lowerDiv = document.createElement("div");
    lowerDiv.className = "lower";
    const heading = document.createElement("h3");
    heading.className = "p-1";
    heading.textContent = school[i].name;
    lowerDiv.appendChild(heading);
    const hr = document.createElement("hr");
    lowerDiv.appendChild(hr);
    const ul = document.createElement("ul");
    ul.className = "p-1";

    let li = document.createElement("li");
    li.innerHTML = `<b>Description:</b> ${school[i].Description}`;
    ul.appendChild(li);
    let pi = document.createElement("li");
    pi.innerHTML = `<b>Description:</b> ${school[i].Benefits}`;
    ul.appendChild(pi);
    let gi = document.createElement("li");
    gi.innerHTML = `<b>Description:</b> ${school[i].Eligibility}`;
    ul.appendChild(gi);
    lowerDiv.appendChild(ul);
    const button = document.createElement("button");
    button.className = "btn-primary text-white";
    button.textContent = "Explore More >>>";
    lowerDiv.appendChild(button);
    cardDiv.appendChild(lowerDiv);
    parentElement.appendChild(cardDiv);
  }
}
